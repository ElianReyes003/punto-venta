            // Si no hay filtro de mes ni de fecha, limpia el filtro en esa columna
            tableMovimientos.column(5).search('').draw();
        }
       
    });

    $('#busqueda').on('keyup', function (e) {
        var filtroBusqueda = $('#busqueda').val();
        tableMovimientos.search(filtroBusqueda).draw();
    });

    $('#limpiarFiltros').on('click', function () {
        $('#articulo,#movimiento, #dia, #mes').val('');
        tableMovimientos.search('').columns().search('').draw();
    });

